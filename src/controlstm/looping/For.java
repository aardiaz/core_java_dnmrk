package controlstm.looping;

public class For {
	
	/*
	 * -------------- for loop --------------
	 * # syntax :
	 *   
	 *       for(initialization; condition; inc/dec){
	 *           
	 *            //statements
	 *       }
	 */
	
	public static void main(String[] args) {
		
		for(int i=1000; i>=500; i--) {
		     System.out.println("Good Evening");
		}
		
	}

}
